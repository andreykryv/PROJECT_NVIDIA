// ui/chartwidget.h — заголовок виджета сравнительных графиков
//
// НАЗНАЧЕНИЕ:
//   ChartWidget объединяет несколько видов QtCharts-графиков для сравнения
//   производительности алгоритмов CPU и GPU. Вкладки внутри виджета
//   позволяют переключаться между типами графиков.
//
// КЛАСС: ChartWidget : public QWidget
//
//   ВКЛАДКИ ГРАФИКОВ (QTabWidget внутри виджета):
//
//   1. "Время выполнения" (ComparisonBarChart)
//      — Групповая столбчатая диаграмма: по X — алгоритмы, по Y — время (мс).
//      — Две серии баров: CPU (синий) и GPU (зелёный).
//      — Для GPU дополнительная серия: "GPU transfer" (серый) — время H2D+D2H.
//      — Подписи значений над каждым баром.
//      — При клике на бар — всплывающий tooltip с детальными метриками.
//
//   2. "Масштабируемость" (ScatterPlotChart)
//      — График зависимости времени от размера массива.
//      — X: размер массива (логарифмическая шкала 1K—100M).
//      — Y: время выполнения (мс, логарифмическая шкала).
//      — Несколько линий — по одной на каждый алгоритм.
//      — Теоретические кривые O(n log n), O(n²), O(n) пунктиром для сравнения.
//
//   3. "Ускорение GPU" (LineChart)
//      — Speedup = T_cpu / T_gpu в зависимости от размера массива.
//      — Горизонтальная пунктирная линия y=1 ("безубыточность GPU").
//      — Цветовая заливка под кривой ускорения.
//
//   4. "Детали GPU" (StackedBarChart)
//      — Составная диаграмма: время GPU разбито на компоненты:
//        H2D transfer | Kernel execution | D2H transfer | Synchronization overhead
//      — Позволяет видеть, сколько времени тратится на пересылку данных vs вычисления.
//
//   ЧЛЕНЫ-ДАННЫЕ:
//     QTabWidget *tabs
//     ComparisonBarChart *barChart
//     ScatterPlotChart *scatterChart
//     QChartView *speedupView
//     QChartView *gpuDetailView
//     QList<BenchmarkResult> results     — история всех запусков
//     int maxStoredResults               — макс. хранимых результатов (default: 50)
//
//   СЛОТЫ:
//     addResult(BenchmarkResult)         — добавить результат и обновить все графики
//     clearResults()                     — очистить историю
//     setMaxResults(int)                 — ограничить число точек на графике
//     exportChart(int tabIndex, QString path) — сохранить график как PNG/SVG
//     highlightAlgorithm(QString name)   — выделить серию данного алгоритма
//
//   МЕТОДЫ:
//     rebuildCharts()                    — перестроить все графики по results
//     updateBarChart()                   — обновить только столбчатую диаграмму
//     updateScatterChart()               — обновить только scatter plot
//     updateSpeedupChart()               — обновить график ускорения
//     updateGPUDetailChart()             — обновить детальный GPU-график
//     setupChartTheme(bool isDark)       — настроить цвета под текущую тему
