// cpu/bubblesort.h — реализация Bubble Sort для CPU
//
// КЛАСС: BubbleSort<T>
//
//   void sort(T* data, int size, CpuSorter::SortCallback cb,
//             std::atomic<bool>& stop):
//     — Внешний цикл i=[0..n-1], внутренний j=[0..n-i-2].
//     — При каждом сравнении: if(shouldEmitFrame) cb(COMPARE, j, j+1, cmp, sw).
//     — При перестановке: std::swap, ++swaps, cb(SWAP, j, j+1, cmp, sw).
//     — В конце прохода: граница отсортированной части i сдвигается.
//       cb(SORTED, n-1-i, -1, ...) — помечаем последний элемент прохода.
//     — Оптимизация: флаг swapped; если за проход не было swap — break.
//     — stop проверяется в начале каждой итерации внешнего цикла.
//
//   ОСОБЕННОСТИ АНИМАЦИИ:
//     — Самый медленный алгоритм — самый наглядный. Рекомендуется запускать
//       с animFPS = 1–10 и maxVisElements ≤ 100 для комфортного просмотра.
//     — "Пузырьки" хорошо видны на цветовой схеме Rainbow.
