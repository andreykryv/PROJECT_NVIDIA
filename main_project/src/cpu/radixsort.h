// cpu/radixsort.h — реализация Radix Sort (LSD) для CPU
//
// КЛАСС: RadixSort<T>  (только для целочисленных типов T)
//
//   void sort(T* data, int size, cb, stop):
//     — LSD (Least Significant Digit): 8-битные разряды, 4 прохода для int32.
//     — На каждом проходе:
//       1. Подсчёт частот: count[256] для текущего байта.
//       2. Prefix sum: превращает count[] в начальные позиции.
//       3. Копирование в буфер с сортировкой по текущему разряду.
//       4. Копирование обратно.
//     — cb(WRITE, i, -1, ...) при записи каждого элемента в буфер.
//     — Для float: специальная обработка знакового бита (IEEE 754 flip).
//     — Не применим напрямую к double в общем случае — используется StdSort.
//
//   ОСОБЕННОСТИ АНИМАЦИИ:
//     — Показывает 4 волны "перетасовки" по байтам, очень наглядно.
//     — Хорошо видна незаметная сортировка: массив кажется хаотичным
//       после первых проходов, но мгновенно упорядочивается на последнем.
