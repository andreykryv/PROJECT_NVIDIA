// cpu/mergesort.h — реализация Merge Sort для CPU
//
// КЛАСС: MergeSort<T>
//
//   void sort(T* data, int size, cb, stop):
//     — Итеративный bottom-up merge sort (без рекурсии, O(log n) памяти для стека).
//     — Буфер tmp: std::vector<T>(size) — выделяется один раз перед сортировкой.
//     — Фазы слияния: width = 1, 2, 4, 8, ..., size/2.
//     — На каждой фазе: merge смежных пар подмассивов длины width.
//
//   void merge(T* data, T* tmp, int lo, int mid, int hi, cb, stop):
//     — Классическое двухуказательное слияние.
//     — cb(COMPARE, i, j, cmp, swaps) при каждом сравнении.
//     — cb(WRITE, targetIdx, -1, ...) при записи элемента в результат.
//     — После merge — обратное копирование tmp→data.
//
//   ОСОБЕННОСТИ АНИМАЦИИ:
//     — Показывает "волны" слияния снизу вверх, очень наглядно.
//     — Подсветка WRITE ярко демонстрирует запись результата слияния.
