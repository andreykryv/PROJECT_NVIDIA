// cpu/stdsort.h — обёртка над std::sort для бенчмаркинга
//
// КЛАСС: StdSort<T>
//
//   void sort(T* data, int size, cb, stop):
//     — Вызывает std::sort(data, data+size).
//     — Callback не вызывается в процессе (std::sort непрозрачен).
//     — После завершения: однократный cb с полным кадром финального состояния.
//     — Это специальный случай: StdSort не анимируется пошагово.
//       В UI показывается только финальное состояние массива.
//       Это подчёркивает разницу: видно, что std::sort — "чёрный ящик".
//
//   ПРИМЕЧАНИЕ:
//     Benchmarking цели: только измерение времени. std::sort — reference point,
//     относительно которого оцениваются другие алгоритмы.
