// visualization/baranimation.cpp — реализация анимации столбцов
//
// update():
//   Применяет spring physics к каждому AnimatedBar.
//   При достижении цели (|current - target| < 0.001) устанавливает current = target.
//   emit animationUpdated() → вызывает repaint виджета.
//
// setTargetFrame():
//   Обновляет targetHeight и targetColor для каждого бара.
//   Новые бары (при изменении размера массива) инициализируются с height=0
//   и анимируются вверх — эффект "появления" массива.
