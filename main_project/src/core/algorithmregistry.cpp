// Конструктор заполняет все записи хардкодом.
// Описания алгоритмов (русскоязычные):
//
// BubbleSort:  "Простейший алгоритм: многократные проходы с заменой соседних
//   элементов. O(n²) — нагляден для обучения, непригоден для prod."
// QuickSort:   "Разделяй и властвуй с трёхпутевым partition (Dutch flag).
//   O(n log n) avg, O(n²) worst. In-place, нестабильный."
// MergeSort:   "Стабильный O(n log n). Рекурсивное слияние. O(n) доп. память."
// HeapSort:    "In-place, O(n log n) worst. Max-heap + извлечение максимума."
// RadixSort:   "Поразрядная сортировка (LSD). O(d·n). Только целые числа."
// StdSort:     "Introsort: quick + heap + insertion. Оптимизирован в libstdc++."
// BitonicSort: "GPU-оптимальный. O(log²n) фаз, каждый шаг полностью параллелен.
//   Размер должен быть степенью двойки."
// ThrustRadix: "NVIDIA Thrust/CUB Radix Sort. Один из быстрейших GPU-сортировщиков.
//   Автоматически выбирает оптимальные параметры под архитектуру."
// GpuQuickSort:"GPU Quick Sort с параллельным partition через prefix scan.
//   Эффективен на нестандартных распределениях."
// CubDevice:   "CUB DeviceRadixSort — низкоуровневый API, максимальная скорость
//   при ручной настройке параметров блока и потоков."
